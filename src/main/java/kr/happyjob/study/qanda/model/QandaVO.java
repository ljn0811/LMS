package kr.happyjob.study.qanda.model;

public class QandaVO {
	private int qna_id;
	private int user_no;
	private String name;
	private String qna_title;
	private String qna_content;
	private String created_at;
	private int qna_cnt;
	
	public int getQna_id() {
		return qna_id;
	}
	public void setQna_id(int qna_id) {
		this.qna_id = qna_id;
	}
	public int getUser_no() {
		return user_no;
	}
	public void setUser_no(int user_no) {
		this.user_no = user_no;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getQna_title() {
		return qna_title;
	}
	public void setQna_title(String qna_title) {
		this.qna_title = qna_title;
	}
	public String getQna_content() {
		return qna_content;
	}
	public void setQna_content(String qna_content) {
		this.qna_content = qna_content;
	}
	public String getCreated_at() {
		return created_at;
	}
	public void setCreated_at(String created_at) {
		this.created_at = created_at;
	}
	public int getQna_cnt() {
		return qna_cnt;
	}
	public void setQna_cnt(int qna_cnt) {
		this.qna_cnt = qna_cnt;
	}
	
	@Override
	public String toString() {
		return "QandaVO [qna_id=" + qna_id + ", user_no=" + user_no + ", name=" + name + ", qna_title=" + qna_title
				+ ", qna_content=" + qna_content + ", created_at=" + created_at + ", qna_cnt=" + qna_cnt + "]";
	}
}
